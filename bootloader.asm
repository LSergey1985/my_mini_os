[org 0x7C00]  ; Загрузчик загружается по адресу 0x7C00

start:
    mov ax, 0x0000
    mov ds, ax
    mov es, ax

    ; Загрузка ядра с диска
    mov ah, 0x02    ; Функция чтения сектора
    mov al, 1       ; Количество секторов для чтения
    mov ch, 0       ; Цилиндр
    mov cl, 2       ; Сектор (начинается с 1)
    mov dh, 0       ; Головка
    mov bx, 0x1000  ; Адрес загрузки ядра
    int 0x13        ; Прерывание диска

    jmp 0x1000:0000 ; Переход к ядру

times 510-($-$$) db 0
dw 0xAA55          ; Сигнатура загрузочного сектора